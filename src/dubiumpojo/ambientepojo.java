package dubiumpojo;

/**
 *
 * @author Cedup 05
 */
public class ambientepojo {
    
    String nome_ambiente, tipo_ambiente, horario_funcionamento;
    int id_ambiente, capacidade;

    public String getNome_ambiente() {
        return nome_ambiente;
    }

    public void setNome_ambiente(String nome_ambiente) {
        this.nome_ambiente = nome_ambiente;
    }

    public String getTipo_ambiente() {
        return tipo_ambiente;
    }

    public void setTipo_ambiente(String tipo_ambiente) {
        this.tipo_ambiente = tipo_ambiente;
    }

    public String getHorario_funcionamento() {
        return horario_funcionamento;
    }

    public void setHorario_funcionamento(String horario_funcionamento) {
        this.horario_funcionamento = horario_funcionamento;
    }

    public int getId_ambiente() {
        return id_ambiente;
    }

    public void setId_ambiente(int id_ambiente) {
        this.id_ambiente = id_ambiente;
    }

    public int getCapacidade() {
        return capacidade;
    }

    public void setCapacidade(int capacidade) {
        this.capacidade = capacidade;
    }
    
    
    
}
